class Solution{
  public:
    //Function to check whether the list is palindrome.
    bool isPalindrome(Node *head)
    {
        //Your code here
        int v1[10000] , co=0;
        Node* t=head;
        
        while(t!=NULL){
            v1[co]=(t->data);
            t=t->next;
            co++;
        }
        
        for(int i=0 ; i<co/2 ; i++){
            if(v1[i]!=v1[co-i-1]){
                return false;
            }
        }
        return true;
    }
};



