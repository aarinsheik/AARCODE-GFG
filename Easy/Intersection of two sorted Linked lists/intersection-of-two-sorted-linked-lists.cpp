class Solution
{
    public:
    Node* findIntersection(Node* head1, Node* head2)
    {
        // code goes here.
        Node* res= new Node(-1);
        Node* a1 = head1 , *a2=head2 , *t=res;
        
        while(a1!=NULL and a2!=NULL){
            
            if(a1->data==a2->data){
                Node* q=new Node(a1->data);
                t->next=q;
                t=t->next;
                
                a1=a1->next;
                a2=a2->next;
            }
            else if(a1->data < a2->data){
                a1=a1->next;
            }
            else{
                a2=a2->next;
            }
        }
        
        return res->next;
    }
};



