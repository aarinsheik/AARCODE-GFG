class Solution
{
    public:
    //Function to remove duplicates from unsorted linked list.
    Node * removeDuplicates( Node *head) 
    {
     // your code goes here
     unordered_map <int,int> mp;
     Node* t=head;
     Node* p=head;
     
     while(t!=NULL){
        if(mp[t->data]){
            Node* k=p->next;
            while(k!=NULL and k->data==t->data){
                k=k->next;
            }
            p->next=k;
            t=p;
        }
        mp[t->data]++;
        p=t;
        t=t->next;
     }
     return head;
    }
};


